        call('/facebookwallshares/add', 
            [
                'user_id' => $AppUI->user_id,           
                'facebook_id' => $AppUI->facebook_id,           
                'keyword' => $search['keyword'],
                'social_id' => $postId,
            ]
        );
        $countPosted++;
    } else {
        batch_info("Post FAIL: {$errorMessage}");
    }    
